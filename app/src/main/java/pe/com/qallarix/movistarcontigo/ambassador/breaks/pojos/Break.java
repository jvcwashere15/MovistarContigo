package pe.com.qallarix.movistarcontigo.ambassador.breaks.pojos;

public class Break {
            private String ambassadorDni;
            private String ambassadorEmail;
            private String ambassadorName;
            private String ambassadorPhone;
            private String codeNetwork;
            private String codeProduct;
            private String codeQuery;
            private String contactPhone;
            private String description;
            private String ownerDni;
            private String ownerEmail;
            private String ownerName;
            private String ownerPhone;

    public Break() {
    }

    public Break(String ambassadorDni, String ambassadorEmail, String ambassadorName, String ambassadorPhone, String codeNetwork, String codeProduct, String codeQuery, String contactPhone, String description, String ownerDni, String ownerEmail, String ownerName, String ownerPhone) {
        this.ambassadorDni = ambassadorDni;
        this.ambassadorEmail = ambassadorEmail;
        this.ambassadorName = ambassadorName;
        this.ambassadorPhone = ambassadorPhone;
        this.codeNetwork = codeNetwork;
        this.codeProduct = codeProduct;
        this.codeQuery = codeQuery;
        this.contactPhone = contactPhone;
        this.description = description;
        this.ownerDni = ownerDni;
        this.ownerEmail = ownerEmail;
        this.ownerName = ownerName;
        this.ownerPhone = ownerPhone;
    }

    public String getAmbassadorDni() {
        return ambassadorDni;
    }

    public void setAmbassadorDni(String ambassadorDni) {
        this.ambassadorDni = ambassadorDni;
    }

    public String getAmbassadorEmail() {
        return ambassadorEmail;
    }

    public void setAmbassadorEmail(String ambassadorEmail) {
        this.ambassadorEmail = ambassadorEmail;
    }

    public String getAmbassadorName() {
        return ambassadorName;
    }

    public void setAmbassadorName(String ambassadorName) {
        this.ambassadorName = ambassadorName;
    }

    public String getAmbassadorPhone() {
        return ambassadorPhone;
    }

    public void setAmbassadorPhone(String ambassadorPhone) {
        this.ambassadorPhone = ambassadorPhone;
    }

    public String getCodeNetwork() {
        return codeNetwork;
    }

    public void setCodeNetwork(String codeNetwork) {
        this.codeNetwork = codeNetwork;
    }

    public String getCodeProduct() {
        return codeProduct;
    }

    public void setCodeProduct(String codeProduct) {
        this.codeProduct = codeProduct;
    }

    public String getCodeQuery() {
        return codeQuery;
    }

    public void setCodeQuery(String codeQuery) {
        this.codeQuery = codeQuery;
    }

    public String getContactPhone() {
        return contactPhone;
    }

    public void setContactPhone(String contactPhone) {
        this.contactPhone = contactPhone;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getOwnerDni() {
        return ownerDni;
    }

    public void setOwnerDni(String ownerDni) {
        this.ownerDni = ownerDni;
    }

    public String getOwnerEmail() {
        return ownerEmail;
    }

    public void setOwnerEmail(String ownerEmail) {
        this.ownerEmail = ownerEmail;
    }

    public String getOwnerName() {
        return ownerName;
    }

    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    public String getOwnerPhone() {
        return ownerPhone;
    }

    public void setOwnerPhone(String ownerPhone) {
        this.ownerPhone = ownerPhone;
    }
}

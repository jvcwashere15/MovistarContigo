package pe.com.qallarix.movistarcontigo.flexplace.miequipo;

public class FlexEquipo {
    private String dateEnd;
    private String dateStart;
    private String employee;
    private long id;

    public String getDateEnd() { return dateEnd; }
    public void setDateEnd(String value) { this.dateEnd = value; }

    public String getDateStart() { return dateStart; }
    public void setDateStart(String value) { this.dateStart = value; }

    public String getEmployee() { return employee; }
    public void setEmployee(String value) { this.employee = value; }

    public long getId() { return id; }
    public void setId(long value) { this.id = value; }
}

package pe.com.qallarix.movistarcontigo.ambassador.breaks.pojos;

public class ProductTypeList {
    private String code;
    private String name;
    private String event;
    private String description;
    private String url;
    private String descriptionTitle;

    public String getCode() { return code; }
    public void setCode(String value) { this.code = value; }

    public String getName() { return name; }
    public void setName(String value) { this.name = value; }

    public String getEvent() { return event; }
    public void setEvent(String value) { this.event = value; }

    public String getDescription() { return description; }
    public void setDescription(String value) { this.description = value; }

    public String getURL() { return url; }
    public void setURL(String value) { this.url = value; }

    public String getDescriptionTitle() { return descriptionTitle; }
    public void setDescriptionTitle(String value) { this.descriptionTitle = value; }
}
